    <h1>Requisitos de inscripción</h1>
    <h3>Primer Año:</h3>
    <h4>Alumnos Nuevos:</h4>
    <ul>• Certificado de 6to Grado (original y copia)</ul>
    <ul>• Constancia de Conducta</ul>
    <ul>• 2 Copias de la Cédula de Identidad del Alumno</ul>
    <ul>• 2 Copias de la Partida de Nacimiento del Alumno</ul>
    <ul>• 2 Fotos tipo carnet del Alumno</ul>
    <ul>• 2 Copias de la Cédula de Identidad del Representante</ul>
    <ul>• 2 Fotos tipo carnet del Representante</ul>
    <h4>Alumnos Provenientes de Otro Plantel (Nuevos o Repitientes):</h4>
    <ul>• Certificación de Calificaciones del plantel de procedencia (original y copia)</ul>
    <ul>• Boleta de Retiro y Constancia de Conducta</ul>
    <ul>• 2 Copias de la Cédula de Identidad</ul>
    <ul>• 2 Copias de la Partida de Nacimiento</ul>
    <ul>• 2 Fotos tipo carnet</ul>
    <ul>• 2 Copias de la Cédula de Identidad del Representante</ul>
    <ul>• 2 Fotos tipo carnet del Representante</ul>
    <h3>Segundo a Cuarto Año:</h3>
    <h4>Alumnos promovidos (prosecución):</h4>
    <ul>• 2 Fotos tipo carnet actualizadas</ul>
    <ul>• 1 Copia de la Cédula</ul>
    <ul>• 1 Copia de la Partida de Nacimiento</ul>
    <h4>Alumnos Provenientes de Otro Plantel (Nuevos o Repitientes):</h4>
    <ul>• Certificación de Calificaciones del plantel de procedencia (original y copia)</ul>
    <ul>• Boleta de Retiro y Constancia de Conducta</ul>
    <ul>• 2 Copias de la Cédula de Identidad</ul>
    <ul>• 2 Copias de la Partida de Nacimiento</ul>
    <ul>• 2 Fotos tipo carnet</ul>
    <ul>• 2 Copias de la Cédula de Identidad del Representante</ul>
    <ul>• 2 Fotos tipo carnet del Representante</ul>
    <h3>Quinto Año:</h3>
    <h4>Alumnos del plantel (promovidos o repitientes):</h4>
    <ul>• 2 Fotos tipo carnet actualizadas</ul>
    <ul>• 1 Copia de la Cédula Ampliada (legible)</ul>
    <ul>• 1 Copia de la Partida de Nacimiento (legible)</ul>
    <ul>• 7 (siete) Timbres Fiscales de 0.1 UT. En caso haber aprobado el 3er Año en otro plantel, sólo deben traer 4 (cuatro) Timbres Fiscales de 0.1 UT. (Repitientes: No aplica).</ul>
    <h4>Alumnos Provenientes de Otro Plantel (Nuevos o Repitientes):</h4>
    <ul>• Certificación de Calificaciones del plantel de procedencia (original y copia)</ul>
    <ul>• Boleta de Retiro y Constancia de Conducta</ul>
    <ul>• 2 Copias de la Cédula de Identidad Ampliadas (legibles)</ul>
    <ul>• 2 Copias de la Partida de Nacimiento (legibles)</ul>
    <ul>• 2 Fotos tipo carnet</ul>
    <ul>• 2 Copias de la Cédula de Identidad del Representante</ul>
    <ul>• 2 Fotos tipo carnet del Representante</ul>
    <ul>• 7 (siete) Timbres Fiscales de 0.1 UT. En caso haber aprobado el 3er Año en otro plantel, sólo deben traer 4 (cuatro) Timbres Fiscales de 0.1 UT. </ul>
    NOTA: La documentación de los alumnos de Quinto Año debe ser inspeccionada cuidadosamente por la Comisión de Revisión de Documentos como requisito para la graduación, por lo que se exige claridad (legibilidad) en los mismos y no deben presentar incongruencias.

